public class Weapon {
    String name;
    int level;
    int attackPoint;
    int priceB;
    int priceS;

    public Weapon(String name, int level, int attackPoint, int priceB, int priceS){
        this.name = name;
        this.level = level;
        this.attackPoint = attackPoint;
        this.priceB = priceB;
        this.priceS = priceS;
    }
}
